function triangles(num) {
var result = "";
var counter = 0;
while (counter < num) {
  result += "#";
  console.log(result);
  counter++;
}
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (var num = 1; num <= 15; num++) {
    if (num % 15 === 0) {
      console.log("fizzbuzz");
    } else if (num % 5 === 0) {
      console.log("buzz");
    } else if (num % 3 === 0) {
      console.log("fizz");
    } else {
      console.log(num);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
  
  var string = "";
   for (var i = 0; i < num; i++) {
     for (var j = 0; j < num; j++) {
       if ((i + j) % 2 === 0) {
         string += " ";
       } else {
         string += "#";
       } 
     }
     string += "\n";
   } 
   console.log(string);
  
}

